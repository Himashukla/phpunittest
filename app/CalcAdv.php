<?php

namespace App;

class CalcAdv
{

    /**
     * Remove an item from the box, or null if the box is empty.
     *
     * @return string
     */
    public function Summation($a,$b,$c)
    {
        return $a + $b + $c;
    }

    public function Subtraction($a,$b,$c)
    {
        return $a - $b - $c;
    }

    public function Multiplication($a,$b,$c)
    {
        return $a * $b * $c;
    }
}
